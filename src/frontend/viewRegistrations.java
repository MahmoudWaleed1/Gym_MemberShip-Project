    /**
     * @param args the command line arguments
     */
    private void addData() {
        backend.TrainerRole trainer = trainerRole.trainer;
        ArrayList<General> registrations = trainer.getListOfRegistrations();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (General r : registrations) {
            String[] s = r.lineRepresentation().split(",");
            Object[] row = {s[0], s[1], s[2], s[3], s[4]};
            model.addRow(row);
        }
    }
    
